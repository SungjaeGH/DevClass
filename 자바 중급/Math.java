import java.util.Random;

public class MathExam {
    public static void main(String[] args) {

        System.out.println("max값 = " + Math.max(5, 20));
        System.out.println("min값 = " + Math.min(5, 20));
        System.out.println("절대값 = " + Math.abs(-10));
        System.out.println("0이상 1.0미만의 랜덤한 실수 = " + Math.random());
        System.out.println("25의 제곱근 = " + Math.sqrt(25));
        System.out.println("2의 10승 = " + Math.pow(2, 10));
        System.out.println("16의 1/2승 = : " + Math.pow(16, 0.5));
        System.out.println("log200 = " + Math.log10(200));
    }
}